export default function SemuaBukuTable({
  data,
  handlePageBerubah,
  handleRowPerPage,
  page,
  rowsPerPage,
  dataCount: count,
}) {
  // Avoid a layout jump when reaching the last page with empty rows.
  const emptyRows =
    page > 0 ? Math.max(0, (1 + page) * rowsPerPage - data[0].count) : 0;

  return (
    <TableContainer component={Paper} sx={{ mt: 2 }}>
      <Table
        sx={{ minWidth: 500, minHeight: 500, maxHeight: 500 }}
        aria-label="custom pagination table"
      >
        <TableHead>
          <TableRow>
            <TableCell>Judul</TableCell>
            <TableCell>Penulis</TableCell>
            <TableCell>Penerbit</TableCell>
            <TableCell>Tahun</TableCell>
            <TableCell>Kategori</TableCell>
          </TableRow>
        </TableHead>
        <TableBody>
          {data.map((row) => (
            <TableRow key={row.id}>
              <TableCell scope="row" style={{ width: 160 }}>
                {row.judul}
              </TableCell>
              <TableCell style={{ width: 160 }}>{row.penulis}</TableCell>
              <TableCell style={{ width: 160 }}>{row.penerbit}</TableCell>
              <TableCell style={{ width: 160 }}>{row.tahun}</TableCell>
              <TableCell style={{ width: 160 }}>{row.id_kategori}</TableCell>
            </TableRow>
          ))}

          {emptyRows > 0 && (
            <TableRow style={{ height: 53 * emptyRows }}>
              <TableCell colSpan={6} />
            </TableRow>
          )}
        </TableBody>
        <TableFooter>
          <TableRow>
            <TablePagination
              rowsPerPageOptions={[5, 10, 25]}
              colSpan={3}
              count={data[0].count}
              rowsPerPage={rowsPerPage}
              page={page}
              SelectProps={{
                inputProps: {
                  'aria-label': 'rows per page',
                },
                native: true,
              }}
              onPageChange={handlePageBerubah}
              onRowsPerPageChange={handleRowPerPage}
              ActionsComponent={TablePaginationActions}
              align="right"
            />
          </TableRow>
        </TableFooter>
      </Table>
    </TableContainer>
  );
}
